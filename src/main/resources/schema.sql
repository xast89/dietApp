DROP TABLE Items IF EXISTS;
CREATE TABLE Items (
  `id` BIGINT AUTO_INCREMENT,
  `cart_id` BIGINT NOT NULL,
  PRIMARY KEY (`id`)
);

DROP TABLE TOY IF EXISTS;
CREATE TABLE TOY (
  TOY_ID               BIGINT AUTO_INCREMENT,
  NAME                    VARCHAR(100) NOT NULL,
  PRIMARY KEY (TOY_ID)
);

